# Loop For

for i in range(6):
    print(i)

# a função range pode ser representada por três formas diferentes:
# range(stop_value): considera o ponto inicial como zero# range(start_value,
# stop_value): gera a sequencia com base no valor inicial e final
# range(start_value, step_size): gera a sequência incrementando
# o valor inicial usando o tamanho do passo até atingir o valor final
# lembrando que grange conta até atingir determinado valor no caso aqui de 0 até 5 
# que dão seis números.