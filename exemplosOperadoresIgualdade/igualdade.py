# Operadores de Igualdade

variavel_um = 1
variavel_dois = 2
variavel_tres = 3

print( 1 == variavel_um)
print( 2 == variavel_tres)

equal = (variavel_um == variavel_dois) # os () são opcionais neste caso, 
# mas ajudam a legibilidade

not_equal = (variavel_um != variavel_dois) 

print("Um é igual a dois? " + str(equal))
print("Um é diferente de dois? " + str(not_equal))

# Operadores de comparação 

print("Um é maior que dois? " + str(variavel_um > variavel_dois))
print("Um é menor do que dois? " + str(variavel_um < variavel_dois))



